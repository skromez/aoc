tower_coords = defaultdict(list)
grid = [list(row) for row in input.strip().split('\n')]
ROWS = len(grid)
COLS = len(grid[0])

for i in range(ROWS):
    for j in range(COLS):
        cell = grid[i][j]
        if cell != '.':
            tower_coords[cell].append((i, j))

antinodes = set()
for key, coords in tower_coords.items():
    for i in range(len(coords)):
        for j in range(i + 1, len(coords)):
            [r1, c1], [r2, c2] = coords[i], coords[j]
            diffr = abs(r1-r2)
            diffc = abs(c1-c2)
            a1 = (r1, c1)
            a2 = (r2, c2)
            while True:
                if 0 <= a1[0] < ROWS and 0 <= a1[1] < COLS:
                    antinodes.add(a1)
                    a1 = (a1[0]-diffr, a1[1]+diffc if c1 > c2 else a1[1]-diffc)
                else:
                    break
            while True:
                if 0 <= a2[0] < ROWS and 0 <= a2[1] < COLS: 
                    antinodes.add(a2)
                    a2 = (a2[0]+diffr, a2[1]+diffc if c2 > c1 else a2[1]-diffc)
                else:
                    break

print(len(antinodes), "RESULT")